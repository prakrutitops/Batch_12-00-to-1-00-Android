fun main()
{

    println("Enter Your id")
    var id : Int = readLine()!!.toInt()

    println("Enter Your Name")
    var name:String = readLine().toString()

    println("Enter Your Salary")
    var salary:Double = readLine()!!.toDouble()

    println("\n Your Id is $id \n Your Name is $name \n Your salary is $salary")


}